<script>
import firebase from "firebase/app"
import "firebase/firestore"
import "firebase/auth"
export default {
    name:"Maiin",
    data(){
        return{
            tags:[],
            tags_c:[],
            inc_tags:[],
            inc_c:[],
            name:"",
            total_:0,
            passing:{
                enable:false,
                last_month_total:0,
            }
        }
    },
    created(){
        this.reload_tag()
    },
    methods:{
        cal_total: function(){
            let token = firebase.auth().currentUser.uid;
            let a = new Date();
            a = a.toISOString()
            let vm_this = this
            firebase.firestore().collection("service").doc(token).collection("wallet").doc(a.split("-")[0] + "-" + a.split("-")[1]).collection("detail").get()
            .then((docs) => {
                var total = 0
                for(var items in docs.docs){
                    total += docs.docs[items].data().count
                }
                vm_this.total_ = total
            })
        },
        reload_tag: function(){
            var token = firebase.auth().currentUser.uid;
            this.name = firebase.auth().currentUser.displayName;
            firebase.firestore().collection("service").doc(token).get()
            .then((doc) => {
                console.log(doc.data());
                this.tags = doc.data().tags
                this.tags_c = doc.data().colors
                this.inc_tags = doc.data().inc_tags
                this.inc_c = doc.data().inc_color
            })
            .catch((error) => {
                console.error(error);
            });
        },
        passing_money: function(){
            this.passing.enable = false
            let token = firebase.auth().currentUser.uid;
            let a = new Date();
            a.setMonth(a.getMonth()-1);
            let b = a.toISOString()
            firebase.firestore().collection("service").doc(token).collection("wallet").doc(b.split("-")[0] + "-" + b.split("-")[1]).collection("detail").get()
            .then((docs) => {
                var total = 0
                for(var items in docs.docs){
                    total += docs.docs[items].data().count
                }
                this.passing.last_month_total = total
                this.passing.enable = true
            })
        }
    }
}
